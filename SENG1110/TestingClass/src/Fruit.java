public class Fruit {
    private String name;
    private double price;
    private int qtd;

    public void setName(String nameIn) {
        name = nameIn;
    }
    public String getName() {
        return name;
    }
    public void setPrice(double priceIn) {
        price = priceIn;
    }
    public double getPrice() {
        return price;
    }
    public void setQtd(int qtdIn) {
        qtd = qtdIn;
    }
    public int getQtd() {
        return qtd;
    }

}
